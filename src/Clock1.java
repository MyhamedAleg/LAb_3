public class Clock1 {
    public static void main(String[] args) {
        int hour = 2;
        int minute = 60;
        for (int i = 0; i <= hour; i++) {
            for (int l = 0; l < minute; l++) {
                System.out.println("hour " + (i ) + " minute " + (l ));
            }
        }
    }
}